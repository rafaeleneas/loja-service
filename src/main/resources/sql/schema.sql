SET MODE POSTGRESQL;

CREATE TABLE IF NOT EXISTS CLIENTES (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME_COMPLETO VARCHAR(120) NOT NULL,
    EMAIL VARCHAR(160) NOT NULL UNIQUE,
    TELEFONE VARCHAR(30),
    CRIADO_EM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS PRODUTOS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR(120) NOT NULL,
    DESCRICAO VARCHAR(2000),
    PRECO NUMERIC(12,2) NOT NULL CHECK (PRECO >= 0),
    ESTOQUE INTEGER NOT NULL DEFAULT 0 CHECK (ESTOQUE >= 0),
    ATIVO BOOLEAN NOT NULL DEFAULT TRUE,
    CRIADO_EM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ATUALIZADO_EM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS PEDIDOS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLIENTE_ID BIGINT NOT NULL,
    IDEMPOTENCY_KEY VARCHAR(100) NOT NULL UNIQUE,
    STATUS VARCHAR(30) NOT NULL DEFAULT 'CRIADO',
    VALOR_TOTAL NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (VALOR_TOTAL >= 0),
    CRIADO_EM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ATUALIZADO_EM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_PEDIDOS_CLIENTE FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTES (ID)
);

CREATE TABLE IF NOT EXISTS ITENS_PEDIDO (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PEDIDO_ID BIGINT NOT NULL,
    PRODUTO_ID BIGINT NOT NULL,
    QUANTIDADE INTEGER NOT NULL CHECK (QUANTIDADE > 0),
    PRECO_UNITARIO NUMERIC(12,2) NOT NULL CHECK (PRECO_UNITARIO >= 0),
    TOTAL_LINHA NUMERIC(12,2) NOT NULL CHECK (TOTAL_LINHA >= 0),
    CONSTRAINT FK_ITENS_PEDIDO_PEDIDO FOREIGN KEY (PEDIDO_ID) REFERENCES PEDIDOS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_ITENS_PEDIDO_PRODUTO FOREIGN KEY (PRODUTO_ID) REFERENCES PRODUTOS (ID)
);

CREATE INDEX IF NOT EXISTS IDX_PRODUTOS_NOME ON PRODUTOS (NOME);
CREATE INDEX IF NOT EXISTS IDX_PEDIDOS_CLIENTE_ID ON PEDIDOS (CLIENTE_ID);
CREATE INDEX IF NOT EXISTS IDX_ITENS_PEDIDO_PEDIDO_ID ON ITENS_PEDIDO (PEDIDO_ID);
CREATE INDEX IF NOT EXISTS IDX_ITENS_PEDIDO_PRODUTO_ID ON ITENS_PEDIDO (PRODUTO_ID);
